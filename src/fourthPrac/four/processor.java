package fourthPrac.four;

public class processor {
    private int cores;
    private int flows;
    private int gen;

    public void setCores(int cores) {
        this.cores = cores;
    }

    public void setFlows(int flows) {
        this.flows = flows;
    }

    public void setGen(int gen) {
        this.gen = gen;
    }

    public int getCores() {
        return cores;
    }

    public int getFlows() {
        return flows;
    }

    public int getGen() {
        return gen;
    }
}
